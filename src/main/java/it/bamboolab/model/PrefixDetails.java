package it.bamboolab.model;

public class PrefixDetails {

	private String sapId;
	private String prefixType;
	private String nextPrefix;
	/**
	 * @return the sapId
	 */
	public String getSapId() {
		return sapId;
	}
	/**
	 * @param sapId the sapId to set
	 */
	public void setSapId(String sapId) {
		this.sapId = sapId;
	}
	/**
	 * @return the prefixType
	 */
	public String getPrefixType() {
		return prefixType;
	}
	/**
	 * @param prefixType the prefixType to set
	 */
	public void setPrefixType(String prefixType) {
		this.prefixType = prefixType;
	}
	/**
	 * @return the nextPrefix
	 */
	public String getNextPrefix() {
		return nextPrefix;
	}
	/**
	 * @param nextPrefix the nextPrefix to set
	 */
	public void setNextPrefix(String nextPrefix) {
		this.nextPrefix = nextPrefix;
	}
	
	
	
}

