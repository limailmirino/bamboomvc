package it.bamboolab.model;

public class SapDetails {

	private String sapId;
	private String sapDescription;
	private String role;
	
	/**
	 * @return the sapId
	 */
	public String getSapId() {
		return sapId;
	}
	/**
	 * @param sapId the sapId to set
	 */
	public void setSapId(String sapId) {
		this.sapId = sapId;
	}
	/**
	 * @return the sapDescription
	 */
	public String getSapDescription() {
		return sapDescription;
	}
	/**
	 * @param sapDescription the sapDescription to set
	 */
	public void setSapDescription(String sapDescription) {
		this.sapDescription = sapDescription;
	}
	/**
	 * @return the role
	 */
	public String getRole() {
		return role;
	}
	/**
	 * @param role the role to set
	 */
	public void setRole(String role) {
		this.role = role;
	}
	
	
	
	
	
}
